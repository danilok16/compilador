package model.entities;

public class Ch {
	private int chInt;
	private char chChar;
	
	
	public Ch() {
		
	}
	public Ch(int chInt) {
	
		this.chInt = chInt;
	}
	public int getChInt() {
		return chInt;
	}
	public void setChInt(int chInt) {
		this.chInt = chInt;
	}
	public char getChChar() {
		return chChar;
	}
	public void setChChar(char chChar) {
		this.chChar = chChar;
	}
	
	
	

}
